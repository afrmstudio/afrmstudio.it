// Navegación suave
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        
        // Chiudi il menu mobile se aperto
        const mobileMenu = document.querySelector('.menu-mobile');
        if (mobileMenu.classList.contains('active')) {
            mobileMenu.classList.remove('active');
        }
        
        document.querySelector(this.getAttribute('href')).scrollIntoView({
            behavior: 'smooth'
        });
    });
});

// Toggle menu mobile
document.addEventListener('DOMContentLoaded', function() {
    const menuButton = document.querySelector('.hamburger-menu');
    const closeButton = document.querySelector('.close-menu');
    const mobileMenu = document.querySelector('.menu-mobile');
    
    if (menuButton && mobileMenu) {
        menuButton.addEventListener('click', function() {
            mobileMenu.classList.add('active');
        });
    }
    
    if (closeButton && mobileMenu) {
        closeButton.addEventListener('click', function() {
            mobileMenu.classList.remove('active');
        });
    }
    
    // Chiudi menu quando si clicca su un link
    document.querySelectorAll('.menu-mobile a[href^="#"]').forEach(link => {
        link.addEventListener('click', function() {
            mobileMenu.classList.remove('active');
        });
    });
    
    // Funzione per cambiare la lingua
    function changeLanguage(language) {
        localStorage.setItem('language', language);
        document.documentElement.lang = language;
        
        document.querySelectorAll('[data-lang-key]').forEach(element => {
            const key = element.getAttribute('data-lang-key');
            
            if (element.hasAttribute('data-lang-attr')) {
                const attr = element.getAttribute('data-lang-attr');
                element.setAttribute(attr, translations[language][key] || key);
            } else {
                element.textContent = translations[language][key] || key;
            }
        });
        
        document.querySelectorAll('.lang-btn').forEach(btn => {
            if (btn.getAttribute('data-lang') === language) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });
    }

    // Event listeners per bottoni di lingua
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const language = this.getAttribute('data-lang');
            changeLanguage(language);
        });
    });
    
    // Inizializzare la lingua
    const savedLanguage = localStorage.getItem('language');
    const initialLanguage = savedLanguage || 'it';
    changeLanguage(initialLanguage);
    
    // Validazione form contatto
    const contactForm = document.getElementById('contact-form');
    if (contactForm) {
        contactForm.addEventListener('submit', function(e) {
            e.preventDefault();
            
            // Basic validation
            let isValid = true;
            const requiredFields = contactForm.querySelectorAll('[required]');
            
            requiredFields.forEach(field => {
                if (!field.value.trim()) {
                    isValid = false;
                    field.classList.add('border-red-500');
                } else {
                    field.classList.remove('border-red-500');
                }
            });
            
            // Email validation
            const emailField = contactForm.querySelector('input[type="email"]');
            if (emailField && emailField.value) {
                const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
                if (!emailPattern.test(emailField.value)) {
                    isValid = false;
                    emailField.classList.add('border-red-500');
                }
            }
            
            if (isValid) {
                // Here you would typically send the form data to a server
                // For now, let's just show a success message
                alert('Grazie per il tuo messaggio! Ti risponderò al più presto.');
                contactForm.reset();
            }
        });
    }
    
    // Effetto reveal allo scroll
    const revealElements = document.querySelectorAll('.reveal');
    
    function checkReveal() {
        for (let i = 0; i < revealElements.length; i++) {
            const windowHeight = window.innerHeight;
            const elementTop = revealElements[i].getBoundingClientRect().top;
            const elementVisible = 150;
            
            if (elementTop < windowHeight - elementVisible) {
                revealElements[i].classList.add('active');
            }
        }
    }
    
    window.addEventListener('scroll', checkReveal);
    checkReveal(); // Controlla elementi visibili al caricamento
});
